class Registro {
    constructor(año, mes, dia, hora, minuto, operario, maquina, modelo, color, talle, cantidad, estado){
        
        
        this.año = año;
        this.mes = mes;
        this.dia = dia;
        this.hora = hora;
        this.minuto = minuto;
        this.operario = operario;
        this.maquina = maquina;
        this.modelo = modelo;
        this.color = color;
        this.talle = talle;
        this.cantidad = parseInt(cantidad);
        this.estado = estado;      
        


    }
   
    
}


